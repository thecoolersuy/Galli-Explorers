export const GATE_UNITS_W = GATE_BITMAP[0].length; // Exactly 46 columns
export const GATE_UNITS_H = GATE_BITMAP.length;    // Exactly 53 rows
const GATE_COLOR = colors.panelNum;

export function getGateLayout(screenWidth, screenHeight) {
  // Balanced constraints to allow scaling up without overflowing aspect ratios
  const maxWidth = screenWidth * 0.85;
  const maxHeight = screenHeight * 0.85;
  
  const unit = Math.max(4, Math.floor(Math.min(maxWidth / GATE_UNITS_W, maxHeight / GATE_UNITS_H)));
  const gateWidth = GATE_UNITS_W * unit;
  const gateHeight = GATE_UNITS_H * unit;
  
  // Center horizontally
  const originX = Math.floor((screenWidth - gateWidth) / 2);
  
  // Align flush to the absolute bottom of the canvas screen
  const originY = screenHeight - gateHeight;

  return { unit, originX, originY, gateWidth, gateHeight, centerX: screenWidth / 2 };
}

export function getGateTextAnchors({ unit, originY, centerX }) {
  return {
    titleY: originY + unit * 19.0,
    statsY: originY + unit * 28.5,
    totalY: originY + unit * 38.0,
    continueY: originY + unit * (GATE_UNITS_H + 2.5),
    centerX,
  };
}

export function drawPixelGate(g, originX, originY, unit, color = GATE_COLOR) {
  g.fillStyle(color, 1);

  for (let row = 0; row < GATE_UNITS_H; row += 1) {
    const line = GATE_BITMAP[row];

    for (let col = 0; col < GATE_UNITS_W; col += 1) {
      if (line[col] !== "#") continue;

      g.fillRect(
        originX + col * unit,
        originY + row * unit,
        unit,
        unit,
      );
    }
  }
}